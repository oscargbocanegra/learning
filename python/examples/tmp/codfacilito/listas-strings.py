my_string = 'Curso de Codigo Facilito'
print (my_string)
# impresion de la posicion del string
print ("posicion " + my_string[9])
# Los valores negativos indican la salida desde la derecha
print (my_string[-2])
# listar  una parte del mensaje
print (my_string[0:8])
# lists con saltos progresivos ejemplo de la posicion 0 hasta la 10 con saltos de 2
print (my_string[0:10:3])